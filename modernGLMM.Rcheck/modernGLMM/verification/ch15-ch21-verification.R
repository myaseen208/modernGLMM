esc <- function(x) {
  gsub("\\|", "\\\\|", as.character(x))
}
md_table <- function(x) {
  x[] <- lapply(x, esc)
  header <- paste0("| ", paste(names(x), collapse = " | "), " |")
  sep <- paste0("|", paste(rep("---", ncol(x)), collapse = "|"), "|")
  body <- apply(x, 1, function(z) {
    paste0("| ", paste(z, collapse = " | "), " |")
  })
  c(header, sep, body)
}

