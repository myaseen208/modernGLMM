legacy_row <- function(example, chapter, note) {
  data.frame(
    Example = example,
    Chapter = chapter,
    `Book Section` = "absent from 2024 2nd edition as exact numbered example",
    `Output Type` = "Legacy example status",
    `Book Value` = "not in 2nd edition",
    `Package Value` = "legacy topic retained",
    Difference = "",
    Status = "UNVERIF",
    Diagnosis = "Example from 1st edition. Not in 2nd edition.",
    `Action Taken` = paste(
      "Classified as LEGACY after searching the extracted 2024 book text;",
      note,
      "No 2024 book numerical comparison is available under this example number."
    ),
    check.names = FALSE
  )
}

no_export_row <- function(chapter, note) {
  data.frame(
    Example = paste0("Chapter", chapter),
    Chapter = chapter,
    `Book Section` = "package inventory",
    `Output Type` = "No exported Exam function",
    `Book Value` = "not applicable",
    `Package Value` = paste0("no Exam", chapter, ".* object present"),
    Difference = "",
    Status = "UNVERIF",
    Diagnosis = paste0(
      "No exported Chapter ", chapter,
      " example function is present in the package source."
    ),
    `Action Taken` = paste(
      "Searched R/, data/, and vignettes/ for exported example objects;",
      note,
      "No numerical verification row can be produced for a non-existent exported example."
    ),
    check.names = FALSE
  )
}

